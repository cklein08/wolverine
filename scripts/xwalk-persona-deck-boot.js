/** NYC + college deck — rebuild after EDS decorateBlocks (same timing as family boot). */
(function () {
  const PRIMARY = '#1DB954';
  const HERO_BG = '#0A1A0F';
  const BADGE = '✦ Personalized for You – Generated by AI';

  const PAGES = {
    '/single-woman-nyc': {
      id: 'single-woman-nyc',
      deviceName: 'Razr',
      planPrefix: 'Your Plan',
      headline: 'You run this city',
      quote:
        'Active New Yorkers love the Razr compact foldable design — perfect for your on-the-go lifestyle in the city.',
      specs: [
        ['6.9" pOLED', '144Hz Display', '5G Ready'],
        ['50MP Camera', '4200mAh Battery', '256GB Storage'],
      ],
    },
    '/college-student': {
      id: 'college-student',
      deviceName: 'Moto G Play',
      planPrefix: 'Student Essential',
      headline: 'Wireless that fits your semester',
      quote:
        'Affordable data and a phone that keeps up with campus life — without breaking the budget.',
      specs: [
        ['6.5" HD+ display', '5G ready', 'All-day battery'],
        ['50MP camera', 'Student budget friendly', 'Campus-ready essentials'],
      ],
    },
  };

  function pageCfg() {
    const path = (location.pathname || '').replace(/\/$/, '') || '/';
    return PAGES[path] || null;
  }

  function injectCss() {
    if (document.getElementById('forge-persona-deck-boot')) return;
    const s = document.createElement('style');
    s.id = 'forge-persona-deck-boot';
    s.textContent = [
      'body.xwalk-persona-offer-page--single-woman-nyc main,body.xwalk-persona-offer-page--college-student main{display:block!important;max-width:none!important;padding:0!important;background:#fff!important;grid-template-columns:1fr!important;color:#111!important}',
      'body.xwalk-persona-offer-page--single-woman-nyc main>div,body.xwalk-persona-offer-page--college-student main>div{background:#fff!important;color:#111!important;min-height:0!important;padding:0!important;position:static!important;overflow:visible!important;text-align:left!important;box-shadow:none!important}',
      '.xwalk-mockup-hero{background:' + HERO_BG + '!important;color:#fff!important}',
      '.xwalk-mockup-hero-grid{display:grid!important;grid-template-columns:1fr 1fr!important;min-height:400px!important}',
      '.xwalk-mockup-hero-left{display:flex!important;flex-direction:column!important;justify-content:center!important;padding:48px 40px 48px 56px!important}',
      '.xwalk-mockup-hero-right{position:relative!important;min-height:400px!important;overflow:hidden!important}',
      '.xwalk-mockup-hero-right img{position:absolute!important;inset:0!important;width:100%!important;height:100%!important;object-fit:cover!important}',
      '.xwalk-mockup-offer-row{display:grid!important;grid-template-columns:1fr minmax(160px,200px)!important;gap:24px!important}',
      '.xwalk-mockup-plan-body{display:grid!important;grid-template-columns:minmax(160px,220px) 1fr!important;gap:32px!important}',
      '.xwalk-mockup-specs{display:grid!important;grid-template-columns:1fr 1fr!important;gap:24px 40px!important}',
      '.xwalk-mockup-plan-price,.xwalk-mockup-device-name,.xwalk-mockup-specs li{color:' + PRIMARY + '!important}',
      '.xwalk-mockup-cta{display:flex!important;align-items:center!important;justify-content:center!important;background:' +
        PRIMARY +
        '!important;color:#000!important;min-height:280px!important;border-radius:14px!important;font-weight:900!important;text-decoration:none!important}',
      '.xwalk-mockup-pill-text{background:#1a1a1a!important;color:#fff!important;border-radius:8px!important;padding:12px 18px!important;margin:0!important}',
    ].join('');
    document.head.appendChild(s);
  }

  function imgFromPicture(picture) {
    const img = picture && picture.querySelector('img');
    if (!img) return null;
    return { src: img.currentSrc || img.src, alt: img.alt || '' };
  }

  function isCta(p) {
    return p && p.tagName === 'P' && p.querySelector('a') && /shop\s*now/i.test(p.textContent || '');
  }

  function isPlanH2(el, cfg) {
    return el && el.tagName === 'H2' && (el.textContent || '').trim().indexOf(cfg.planPrefix) === 0;
  }

  function parsePlanTitle(text, cfg) {
    const t = (text || '').trim();
    const m = t.match(/\$[\d.]+(?:\/mo)?/i);
    const price = m ? m[0] : '';
    const title = price ? t.replace(price, '').trim() : cfg.planPrefix;
    return { title: title, price: price };
  }

  function parseSection(section, cfg) {
    const nodes = [...section.children];
    let headline = cfg.headline;
    let quote = cfg.quote;
    let heroSrc = '';
    let heroAlt = '';
    let logoSrc = '';
    const offers = [];
    let i = 0;

    while (i < nodes.length) {
      const el = nodes[i];
      if (el.tagName === 'H1') {
        headline = el.textContent.trim() || headline;
        i += 1;
        continue;
      }
      if (el.tagName === 'P' && el.querySelector('em') && !el.querySelector('picture')) {
        quote = (el.querySelector('em') || el).textContent.trim() || quote;
        i += 1;
        continue;
      }
      if (el.tagName === 'P' && el.querySelector('picture')) {
        const pic = imgFromPicture(el.querySelector('picture'));
        if (el.querySelector('a[href="/"]') && pic) logoSrc = pic.src;
        else if (pic && !heroSrc) {
          heroSrc = pic.src;
          heroAlt = pic.alt;
        }
        i += 1;
        continue;
      }
      if (!isPlanH2(el, cfg)) {
        i += 1;
        continue;
      }

      const plan = parsePlanTitle(el.textContent, cfg);
      const pills = [];
      let deviceSrc = '';
      let deviceAlt = cfg.deviceName;
      let deviceName = cfg.deviceName;
      let devicePrice = '';
      let ctaHref = '/phones';
      let specCols = cfg.specs;
      i += 1;

      while (i < nodes.length && !isPlanH2(nodes[i], cfg)) {
        const node = nodes[i];
        if (isCta(node)) {
          ctaHref = node.querySelector('a').getAttribute('href') || ctaHref;
          i += 1;
          break;
        }
        if (node.tagName === 'P' && node.querySelector('picture')) {
          const pic = imgFromPicture(node.querySelector('picture'));
          if (pic) {
            deviceSrc = pic.src;
            deviceAlt = pic.alt || deviceAlt;
          }
          i += 1;
          continue;
        }
        if (node.tagName === 'P' && node.querySelector('strong')) {
          devicePrice = (node.querySelector('strong') || node).textContent.trim();
          i += 1;
          continue;
        }
        if (node.tagName === 'P' && !node.querySelector('picture') && !node.querySelector('a')) {
          const text = (node.textContent || '').trim();
          if (/personalized for you/i.test(text)) {
            i += 1;
            continue;
          }
          if (text === cfg.deviceName) {
            deviceName = text;
            i += 1;
            continue;
          }
          if (text.length > 8) pills.push(text);
          i += 1;
          continue;
        }
        if (node.tagName === 'UL') {
          const cols = [];
          while (i < nodes.length && nodes[i].tagName === 'UL') {
            cols.push([...nodes[i].querySelectorAll('li')].map((li) => li.textContent.trim()));
            i += 1;
          }
          if (cols.length) specCols = cols;
          continue;
        }
        i += 1;
      }

      offers.push({
        title: plan.title,
        price: plan.price,
        pill: pills[pills.length - 1] || pills[0] || '',
        deviceSrc: deviceSrc,
        deviceAlt: deviceAlt,
        deviceName: deviceName,
        devicePrice: devicePrice,
        specCols: specCols,
        ctaHref: ctaHref,
      });
    }

    return { headline: headline, quote: quote, heroSrc: heroSrc, heroAlt: heroAlt, logoSrc: logoSrc, offers: offers };
  }

  function specsHtml(cols) {
    return cols
      .map(function (items) {
        return '<ul>' + items.map(function (li) { return '<li>' + li + '</li>'; }).join('') + '</ul>';
      })
      .join('');
  }

  function offerRow(offer) {
    const pill = offer.pill
      ? '<div class="xwalk-mockup-plan-pill"><p class="xwalk-mockup-pill-text">' + offer.pill + '</p></div>'
      : '';
    const deviceImg = offer.deviceSrc
      ? '<img src="' + offer.deviceSrc + '" alt="' + offer.deviceAlt + '" loading="lazy" decoding="async">'
      : '';
    return (
      '<div class="xwalk-mockup-offer-row">' +
      '<div class="xwalk-mockup-offer-main">' +
      '<div class="xwalk-mockup-plan-head"><h2 class="xwalk-mockup-plan-title">' +
      offer.title +
      ' <span class="xwalk-mockup-plan-price">' +
      offer.price +
      '</span></h2>' +
      pill +
      '</div><div class="xwalk-mockup-plan-body"><div class="xwalk-mockup-device">' +
      deviceImg +
      '<p class="xwalk-mockup-device-name">' +
      offer.deviceName +
      '</p></div><div class="xwalk-mockup-specs">' +
      specsHtml(offer.specCols) +
      '</div></div><p class="xwalk-mockup-device-price"><strong>' +
      offer.devicePrice +
      '</strong></p></div><a class="xwalk-mockup-cta" href="' +
      offer.ctaHref +
      '">Shop Now →</a></div>'
    );
  }

  function build(parsed, cfg) {
    const logo =
      parsed.logoSrc ||
      '/media_1a7ada75bcb9954749625b6afdc636958c6f4b3dd.svg?width=64&format=svg&optimize=medium';
    const heroImg = parsed.heroSrc
      ? '<img src="' + parsed.heroSrc + '" alt="' + parsed.heroAlt + '" loading="eager" decoding="async">'
      : '';
    const variants = parsed.offers
      .map(function (offer, idx) {
        const hidden = idx > 0 ? ' hidden' : '';
        return '<div data-forge-variant="offer-' + idx + '"' + hidden + '>' + offerRow(offer) + '</div>';
      })
      .join('');

    const root = document.createElement('div');
    root.className = 'xwalk-persona-mockup xwalk-persona-mockup--' + cfg.id;
    root.dataset.personaId = cfg.id;
    root.innerHTML =
      '<section class="xwalk-mockup-hero"><div class="xwalk-mockup-hero-grid">' +
      '<div class="xwalk-mockup-hero-left"><p class="xwalk-mockup-logo"><a href="/"><img src="' +
      logo +
      '" alt="Wolverine Mobile" width="40" height="40"></a></p><p class="xwalk-mockup-ai-badge">' +
      BADGE +
      '</p><h1>' +
      parsed.headline +
      '</h1></div><div class="xwalk-mockup-hero-right">' +
      heroImg +
      '</div></div></section><div class="xwalk-mockup-green-bar">' +
      BADGE +
      '</div><section class="xwalk-mockup-white"><p class="xwalk-mockup-quote"><em>' +
      parsed.quote +
      '</em></p><div class="xwalk-mockup-offers">' +
      variants +
      '</div></section>';
    return root;
  }

  function findSection() {
    return (
      document.querySelector('.xwalk-persona-deck .default-content-wrapper') ||
      document.querySelector('.xwalk-persona-deck > div > div') ||
      document.querySelector('main > .section > div') ||
      document.querySelector('main > div')
    );
  }

  function run() {
    const cfg = pageCfg();
    if (!cfg || !document.body) return;

    injectCss();
    document.body.classList.add('xwalk-persona-offer-page', 'xwalk-persona-offer-page--' + cfg.id);
    document.documentElement.dataset.forgePersona = cfg.id;

    const main = document.querySelector('main');
    if (main) {
      main.classList.remove('xwalk-boost-main');
      main.style.cssText = 'display:block;max-width:none;padding:0;background:#fff;color:#111;';
      document.body.classList.remove('xwalk-boost-page');
    }

    if (document.querySelector('.xwalk-persona-mockup .xwalk-mockup-offer-row')) return;

    const section = findSection();
    if (!section || !section.querySelector('h1') || !section.querySelector('h2')) return;

    const parsed = parseSection(section, cfg);
    if (!parsed.offers.length) return;

    const built = build(parsed, cfg);
    if (!built.querySelector('.xwalk-mockup-offer-row')) return;
    section.replaceChildren(built);
  }

  function schedule() {
    run();
  }

  document.addEventListener('DOMContentLoaded', schedule);
  document.addEventListener('aem:loaded', schedule);
  [100, 400, 800, 1500, 3000, 5000].forEach(function (ms) {
    setTimeout(schedule, ms);
  });

  var mainEl = document.querySelector('main');
  if (mainEl) {
    new MutationObserver(schedule).observe(mainEl, { childList: true, subtree: true });
  }
})();
