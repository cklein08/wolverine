function imgFromPicture(picture) {
  const img = picture?.querySelector('img');
  if (!img) return null;
  return { src: img.currentSrc || img.src, alt: img.alt || '' };
}

function isCtaParagraph(p) {
  return p?.tagName === 'P' && p.querySelector('a') && /shop\s*now/i.test(p.textContent || '');
}

function isPlanH2(el, cfg) {
  if (el?.tagName !== 'H2') return false;
  const t = (el.textContent || '').trim();
  return t.startsWith(cfg.planTitlePrefix);
}

function parsePlanTitle(h2Text, cfg) {
  const t = (h2Text || '').trim();
  const priceMatch = t.match(/\$[\d.]+(?:\/mo)?/i);
  const price = priceMatch ? priceMatch[0] : '';
  const title = price ? t.replace(price, '').trim() : cfg.planTitlePrefix;
  return { title, price };
}

function collectSpecs(section, startIdx, nodes) {
  const cols = [];
  let i = startIdx;
  while (i < nodes.length && nodes[i].tagName === 'UL') {
    cols.push([...nodes[i].querySelectorAll('li')].map((li) => li.textContent.trim()));
    i += 1;
  }
  return { cols, nextIdx: i };
}

function parseDeckOffers(section, cfg) {
  const nodes = [...section.children];
  let headline = cfg.defaultHeadline;
  let quote = cfg.defaultQuote;
  let heroSrc = '';
  let heroAlt = '';
  let logoSrc = '';
  const offers = [];
  let i = 0;

  while (i < nodes.length) {
    const el = nodes[i];
    if (el.tagName === 'H1') {
      headline = el.textContent.trim() || headline;
      i += 1;
      continue;
    }
    if (el.tagName === 'P' && el.querySelector('em') && !el.querySelector('picture')) {
      quote = (el.querySelector('em')?.textContent || el.textContent || '').trim() || quote;
      i += 1;
      continue;
    }
    if (el.tagName === 'P' && el.querySelector('picture')) {
      const pic = imgFromPicture(el.querySelector('picture'));
      const isLogo = el.querySelector('a[href="/"]');
      if (isLogo && pic) {
        logoSrc = pic.src;
      } else if (pic && !heroSrc) {
        heroSrc = pic.src;
        heroAlt = pic.alt;
      }
      i += 1;
      continue;
    }
    if (!isPlanH2(el, cfg)) {
      i += 1;
      continue;
    }

    const { title, price } = parsePlanTitle(el.textContent, cfg);
    const pills = [];
    let deviceSrc = '';
    let deviceAlt = cfg.deviceName;
    let deviceName = cfg.deviceName;
    let devicePrice = '';
    let ctaHref = '/phones';
    let specCols = cfg.defaultSpecs;
    i += 1;

    while (i < nodes.length && !isPlanH2(nodes[i], cfg)) {
      const node = nodes[i];
      if (isCtaParagraph(node)) {
        ctaHref = node.querySelector('a')?.getAttribute('href') || ctaHref;
        i += 1;
        break;
      }
      if (node.tagName === 'P' && node.querySelector('picture')) {
        const pic = imgFromPicture(node.querySelector('picture'));
        if (pic) {
          deviceSrc = pic.src;
          deviceAlt = pic.alt || deviceAlt;
        }
        i += 1;
        continue;
      }
      if (node.tagName === 'P' && node.querySelector('strong')) {
        devicePrice = (node.querySelector('strong')?.textContent || node.textContent || '').trim();
        i += 1;
        continue;
      }
      if (node.tagName === 'P' && !node.querySelector('picture') && !node.querySelector('a')) {
        const text = (node.textContent || '').trim();
        if (/personalized for you/i.test(text)) {
          i += 1;
          continue;
        }
        if (text === cfg.deviceName || /^moto|^razr/i.test(text)) {
          deviceName = text;
          i += 1;
          continue;
        }
        if (text.length > 8) pills.push(text);
        i += 1;
        continue;
      }
      if (node.tagName === 'UL') {
        const collected = collectSpecs(section, i, nodes);
        if (collected.cols.length) specCols = collected.cols;
        i = collected.nextIdx;
        continue;
      }
      i += 1;
    }

    offers.push({
      title,
      price,
      pill: pills[pills.length - 1] || pills[0] || '',
      deviceSrc,
      deviceAlt,
      deviceName,
      devicePrice,
      specCols,
      ctaHref,
    });
  }

  return { headline, quote, heroSrc, heroAlt, logoSrc, offers };
}

function buildSpecsHtml(cols) {
  return cols
    .map(
      (items) =>
        `<ul>${items.map((li) => `<li>${li}</li>`).join('')}</ul>`,
    )
    .join('');
}

function buildOfferRow(offer) {
  const pill = offer.pill
    ? `<div class="xwalk-mockup-plan-pill"><p class="xwalk-mockup-pill-text">${offer.pill}</p></div>`
    : '';
  const deviceImg = offer.deviceSrc
    ? `<img src="${offer.deviceSrc}" alt="${offer.deviceAlt}" loading="lazy" decoding="async">`
    : '';
  return `<div class="xwalk-mockup-offer-row">
  <div class="xwalk-mockup-offer-main">
    <div class="xwalk-mockup-plan-head">
      <h2 class="xwalk-mockup-plan-title">${offer.title} <span class="xwalk-mockup-plan-price">${offer.price}</span></h2>
      ${pill}
    </div>
    <div class="xwalk-mockup-plan-body">
      <div class="xwalk-mockup-device">
        ${deviceImg}
        <p class="xwalk-mockup-device-name">${offer.deviceName}</p>
      </div>
      <div class="xwalk-mockup-specs">${buildSpecsHtml(offer.specCols)}</div>
    </div>
    <p class="xwalk-mockup-device-price"><strong>${offer.devicePrice}</strong></p>
  </div>
  <a class="xwalk-mockup-cta" href="${offer.ctaHref}">Shop Now →</a>
</div>`;
}

function buildDeckDom(parsed, personaId) {
  const cfg = DECK_PAGES[personaId];
  const logo = parsed.logoSrc || '/media_1a7ada75bcb9954749625b6afdc636958c6f4b3dd.svg?width=64&format=svg&optimize=medium';
  const heroImg = parsed.heroSrc
    ? `<img src="${parsed.heroSrc}" alt="${parsed.heroAlt}" loading="eager" decoding="async">`
    : '';

  const variantShells = (parsed.offers.length ? parsed.offers : [{ title: cfg.planTitlePrefix, price: '', specCols: cfg.defaultSpecs, deviceName: cfg.deviceName, devicePrice: '', ctaHref: '/phones', pill: '' }])
    .map((offer, idx) => {
      const hidden = idx > 0 ? ' hidden' : '';
      return `<div data-forge-variant="offer-${idx}"${hidden}>${buildOfferRow(offer)}</div>`;
    })
    .join('');

  const root = document.createElement('div');
  root.className = `xwalk-persona-mockup xwalk-persona-mockup--${personaId}`;
  root.dataset.personaId = personaId;
  root.innerHTML = `
<section class="xwalk-mockup-hero">
  <div class="xwalk-mockup-hero-grid">
    <div class="xwalk-mockup-hero-left">
      <p class="xwalk-mockup-logo"><a href="/"><img src="${logo}" alt="Wolverine Mobile" width="40" height="40"></a></p>
      <p class="xwalk-mockup-ai-badge">${BADGE}</p>
      <h1>${parsed.headline}</h1>
    </div>
    <div class="xwalk-mockup-hero-right">${heroImg}</div>
  </div>
</section>
<div class="xwalk-mockup-green-bar">${BADGE}</div>
<section class="xwalk-mockup-white">
  <p class="xwalk-mockup-quote"><em>${parsed.quote}</em></p>
  <div class="xwalk-mockup-offers">${variantShells}</div>
</section>`;
  return root;
}

function findContentRoot(main) {
  if (main.querySelector('.xwalk-persona-mockup .xwalk-mockup-offer-row')) return null;
  const direct =
    main.querySelector(':scope > .section > div') ||
    main.querySelector(':scope > div');
  if (direct?.querySelector('h1') && direct?.querySelector('h2')) return direct;
  const roots = [
    ...main.querySelectorAll('.xwalk-persona-deck .default-content-wrapper'),
    ...main.querySelectorAll('.xwalk-persona-deck > div > div'),
    ...main.querySelectorAll('.xwalk-persona-mockup-page .default-content-wrapper'),
    ...main.querySelectorAll(':scope > .section > div > div'),
    ...main.querySelectorAll(':scope > .section > div'),
    ...main.querySelectorAll(':scope > div'),
  ];
  for (const el of roots) {
    if (el.querySelector('h1') && el.querySelector('h2')) return el;
  }
  return null;
}

/** @param {HTMLElement} main */
export function decoratePersonaDeckMain(main) {
  const personaId = deckPersonaId();
  if (!personaId || !main) return;

  const cfg = DECK_PAGES[personaId];
  injectDeckStyles(document);
  document.body.classList.add('xwalk-persona-offer-page', `xwalk-persona-offer-page--${personaId}`);
  main.classList.remove('xwalk-boost-main');
  main.style.display = 'block';
  main.style.maxWidth = 'none';
  main.style.padding = '0';
  main.style.background = '#fff';

  if (main.querySelector('.xwalk-persona-mockup .xwalk-mockup-offer-row')) return;

  const section = findContentRoot(main);
  if (!section) return;

  const parsed = parseDeckOffers(section, cfg);
  if (!parsed.offers.length && !parsed.headline) return;

  const built = buildDeckDom(parsed, personaId);
  const host = section.closest('.default-content-wrapper') || section;
  host.replaceChildren(built);

  const block = main.querySelector('.xwalk-persona-deck, .xwalk-persona-mockup-page');
  if (block) block.classList.add('xwalk-persona-deck-ready');
}

/** Retry deck decorate when main mutates (matches family boot). */
export function schedulePersonaDeckDecorate() {
  const personaId = deckPersonaId();
  if (!personaId) return;
  const run = () => {
    const main = document.querySelector('main');
    if (main) decoratePersonaDeckMain(main);
  };
  run();
  document.addEventListener('DOMContentLoaded', run);
  document.addEventListener('aem:loaded', run);
  [100, 400, 800, 1500, 3000].forEach((ms) => setTimeout(run, ms));
  const main = document.querySelector('main');
  if (main && !main.dataset.forgeDeckObserve) {
    main.dataset.forgeDeckObserve = '1';
    new MutationObserver(run).observe(main, { childList: true, subtree: true });
  }
}

if (typeof window !== 'undefined' && deckPersonaId()) {
  schedulePersonaDeckDecorate();
}
