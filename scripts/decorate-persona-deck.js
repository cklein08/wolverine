function deckPersonaId() {
  const path = (window.location?.pathname || '').replace(/\/$/, '');
  if (path === '/single-woman-nyc') return 'single-woman-nyc';
  if (path === '/college-student') return 'college-student';
  return null;
}

function isDeckPage() {
  return Boolean(deckPersonaId());
}

